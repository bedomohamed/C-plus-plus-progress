//Check whether a Number is Even or Odd using if else
#include<iostream>
using namespace std;
int main(){
    int x;
    cout <<"Please enter a number"<<endl;
    cin >> x;
    if (x % 2 == 0){
        cout<<"That's an even number"<<endl;
    }else{
        cout<<"That's and odd number"<<endl;
    }

}
